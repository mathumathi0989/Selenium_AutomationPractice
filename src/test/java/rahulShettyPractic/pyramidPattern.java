package rahulShettyPractic;

public class pyramidPattern {
//Program for printing pyramid pattern in java
	/*
	 * 
	 * *
	 * * *
	 * * * *
	 */
	public static void main(String[] args) {
		
		for(int i=0; i<5; i++) {
			for(int j=0; j<=i; j++) {
				System.out.print("*");
				System.out.print(" ");
			}
			System.out.println("");
		}
		
		
		
		
		
		
	}

}
